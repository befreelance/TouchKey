#include "uart.h"

void UartInit()
{

	PCON &= 0x7F;		//波特率不倍速
	SCON = 0x50;		//8位数据,可变波特率
	AUXR &= 0xBF;		//定时器1时钟为Fosc/12,即12T
	AUXR &= 0xFE;		//串口1选择定时器1为波特率发生器
	TMOD &= 0x0F;		//清除定时器1模式位
	TMOD |= 0x20;		//设定定时器1为8位自动重装方式
	TL1 = 0xFD;		//设定定时初值
	TH1 = 0xFD;		//设定定时器重装值
	ET1 = 0;		//禁止定时器1中断
	ES=1;
	TR1 = 1;		//启动定时器1
}
void Tx_Data(uchar txData)
{
	
	SBUF=txData;			// write data x to SBUF
	while(0==TI);
	TI=0;
	
}

void Tx_String(char *s)
{
	while(*s)
	{
		Tx_Data(*s++);
	}
}

void Tx_Data485(uchar txData)
{
	P36=1;
	P37=1;
//	Delay1ms();
	SBUF=txData;			// write data x to SBUF
	while(!TI);
	TI=0;
//	Delay1ms();
//
	P36=0;
	P37=0;
}

void Tx_String485(char *s)
{


	while(*s)
	{
		Tx_Data485(*s++);
		Delay1ms();
	}

	Delay50us();

}
